    private void btnAtualizaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizaActionPerformed
        // Atualiza os dados do formulario.
        DaoSql ds = new DaoSql(null,'P');
        EntradaPadrao in = new EntradaPadrao();
        
        Pessoa p = new Pessoa();
        Enderecos ed = new Enderecos();
        Contato co = new Contato();

            //Atualiza dados pessoais.
            p.setNome(txtNome.getText());
            p.setAniversario(txtDtAniversario.getText());
            p.setReligiao(txtReligiao.getText());
            p.setResponsavel(txtMissionario.getText());
            p.setObservacao(txtAreaObs.getText());
            if (cboProgItem.equals("OUTROS"))
            {
               p.setPrograma(txtAreaMat.getText());
            } else {
               p.setPrograma(cboProgItem);
            }
            if (indEstudo == 'S'){
                p.setIndEstudo('S');
            } else {
                p.setIndEstudo('N');
            }
            p.setFxEtaria(cboFxEtaria.getSelectedItem().toString());
            p.setOrigem(txtOrigem.getText());
            p.setId(Integer.parseInt(txtNumMatric.getText()));

            ed.setCep(txtNumCep.getText());
            ed.setEndereco(txtEndereco.getText());
            ed.setBairro(txtBairro.getText());
            ed.setCidade(cboUf.getSelectedItem().toString());
            ed.setCod_rua(Integer.parseInt(txtCodRua.getText()));
            ed.setTxt_complamento(txtComplemento.getText());
            ed.setCodEndereco(idEndereco);

            //Insere Contatos.
            co.setNumCelular(txtNumCel.getText());
            co.setNumFixo(txtNumFixo.getText());
            co.setIndZap(indZap);
            co.setEndEmail(txtEmail.getText());
            co.setNomeOperadora(cboOperadora.getSelectedItem().toString());
            co.setIdContato(idContato);
            
            if (!ds.sqlAtualiza(p, ed, co))
            {
              in.exibeMensagem("Erro ao atualizar os dados!", "Erro de Atualização", 0);
            } else {
              in.exibeMensagem("Dados atualizados com sucesso.", "Atualização realizada", 1);
              this.atualizaDadosForm();
            }  
            
    }//GEN-LAST:event_btnAtualizaActionPerformed

