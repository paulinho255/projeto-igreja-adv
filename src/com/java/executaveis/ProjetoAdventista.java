    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // Botão Vai.
        EntradaPadrao in = new EntradaPadrao();
        String query = "select p.idpessoa"                                                                   +
                       "      ,p.nome_pessoa"                                                                +
                       "      ,to_char(p.mes_ano_aniversario,'dd/mm') as mes_ano_aniversario"                +
                       "      ,p.religiao"                                                                   +
                       "      ,p.responsavel"                                                                +
                       "      ,p.obs"                                                                        +
                       "      ,p.prog_estudo"                                                                +
                       "      ,p.ind_estudo"                                                                 +
                       "      ,p.faixa_etaria"                                                               +
                       "      ,p.txt_origem "                                                                +
                       "      ,c.idcontato "                                                                 +
                       "      ,to_char(c.dt_contato,'dd/mm/yyyy') as dt_contato"                             +
                       "      ,e.txt_complemento"                                                            +
                       "      ,c.num_celular"                                                                +
                       "      ,c.num_fixo"                                                                   +
                       "      ,c.ind_zap"                                                                    +
                       "      ,c.operadora"                                                                  +
                       "      ,c.email"                                                                      +
                       "      ,e.cod_endereco"                                                               +  
                       "      ,e.endereco"                                                                   +
                       "      ,e.cep"                                                                        +
                       "      ,e.bairro"                                                                     +
                       "      ,e.cidade"                                                                     +
                       "      ,e.cod_rua"                                                                    +  
                       "  from pessoa p  "                                                                   +
                       "     , contato c"                                                                    +
                       "     , enderecos e "                                                                 +
                       "   where p.contato_idcontato = c.idcontato "                                         +
                       "     and p.enderecos_cod_endereco = e.cod_endereco";
                    if (!txtPesqNome.getText().equals("")){
                       query = query + " and upper(p.nome_pessoa) like '%"+txtPesqNome.getText().toUpperCase()+"%'";
                    }
                   
                    if (!txtPesqId.getText().equals("")){
                        query = query + " and p.idpessoa = to_number('"+txtPesqId.getText()+"','999999999')";
                    }   
                    
                    if (!txtPesqNome.getText().equals("") || !txtPesqId.getText().equals("")) { 
                        this.limpaTexto();
                        pessoas = null;
                        contatos = null;
                        enderecos = null;
                        qtdRegF = 0;
                        qtdRegB = 0;
                        qtdRegCursor = 0;

                        DaoSql ds = new DaoSql(query, 'P');
                        pessoas = ds.sqlConsulta();
                        contatos = ds.sqlConsultaCo();
                        enderecos = ds.sqlConsultaEnd();
                        qtdRegCursor = pessoas.size();
                        ds = null;

                        if (qtdRegCursor > 0){
                            in.exibeMensagem("Foram localizadas: " +qtdRegCursor+" Ocorrências", "Quantidade Registros", 1);
                            if (qtdRegCursor == 1){
                               btnAvancar.setEnabled(true);
                               btnDeleta.setEnabled(true);
                               btnAtualiza.setEnabled(true);
                            } else {
                               btnAvancar.setEnabled(true);
                               btnVoltar.setEnabled(false);
                               btnAtualiza.setEnabled(true);
                               btnDeleta.setEnabled(true);
                            }
                            btnInsere.setEnabled(false);
                        } else {
                            in.exibeMensagem("Nenhum registro localizado.", "Quantidade Registros", 2);
                            btnAvancar.setEnabled(false);
                            btnVoltar.setEnabled(false);
                            btnAtualiza.setEnabled(false);
                            btnDeleta.setEnabled(false);
                            btnInsere.setEnabled(true);
                        }
                   }
    }//GEN-LAST:event_jButton3ActionPerformed

