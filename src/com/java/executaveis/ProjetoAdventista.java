    private void btnInsereActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsereActionPerformed
        // Botão Inserir.
        DaoSql ds = new DaoSql(null,'P');
        numSeq = ds.getSquencia();
        Pessoa p = new Pessoa();
        Enderecos ed = new Enderecos();
        Contato co = new Contato();
        EntradaPadrao in = new EntradaPadrao();
        boolean inserido;
        
        //Insere endereços.
        if (!vNumMatric.equals("-1")){
            ed.setCodEndereco(numSeq);
            ed.setCep(txtNumCep.getText());
            ed.setEndereco(txtEndereco.getText());
            ed.setBairro(txtBairro.getText());
            ed.setCidade(cboUf.getSelectedItem().toString());
            ed.setCod_rua(Integer.parseInt(txtCodRua.getText()));
            ed.setTxt_complamento(txtComplemento.getText());

            //Insere Contatos.
            co.setIdContato(numSeq);
            co.setDtContato(in.getDataLocal(null,null));
            co.setNumCelular(vNumCel);
            co.setNumFixo(vNumFixo);
            
            co.setIndZap(indZap);
            co.setEndEmail(txtEmail.getText());
            co.setNomeOperadora(cboOperadora.getSelectedItem().toString());

            //Insere dados pessoais.
            p.setId(Integer.parseInt(vNumMatric));
            p.setContato_id(numSeq);
            p.setEndereco_id(numSeq);
            p.setNome(txtNome.getText());
            p.setAniversario(txtDtAniversario.getText());
            p.setReligiao(txtReligiao.getText());
            p.setResponsavel(txtMissionario.getText());
            p.setObservacao(txtAreaObs.getText());
            if (cboProgItem.equals("OUTROS"))
            {
               p.setPrograma(txtAreaMat.getText());
            } else {
               p.setPrograma(cboProgItem);
            }
            if (indEstudo == 'S'){
                p.setIndEstudo('S');
            } else {
                p.setIndEstudo('N');
            }
            p.setFxEtaria(cboFxEtaria.getSelectedItem().toString());
            p.setOrigem(txtOrigem.getText());
            
            inserido = ds.sqlInsere(p,ed,co);

            if (!inserido)
            {
               in.exibeMensagem("Erro ao inserir os dados!", "Erro de inserção", 0);
            } else {
              in.exibeMensagem("Dados inseridos com sucesso.", "Cadastro realizado", 1);
              
              /*Limpa campos.*/
              this.limpaTexto();
              
              /*Insere nova matricula.*/
              vNumMatric = ds.getMatricula();
            if (!vNumMatric.equals("-1")){
              txtNumMatric.setText(vNumMatric);
            } 
            }
        }
    }//GEN-LAST:event_btnInsereActionPerformed
